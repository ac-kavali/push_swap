#include "libft.h"

int	ft_str_isdigit(char *str)
{
	int	n;

	n = 0;
	if (str[n] == '-' || str[n] == '+')
		n++;
	while (str[n])
	{
		if (!ft_isdigit(str[n]))
			return (0);
		n++;
	}
	return (1);
}
