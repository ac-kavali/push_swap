#include "../libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (*lst == 0)
		*lst = new;
	else
	{
		while ((*lst)->next != 0)
			lst = &(*lst)->next;
		(*lst)->next = new;
	}
}
