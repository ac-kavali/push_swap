#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	int	len;

	len = 1;
	if (lst)
	{
		while (lst->next != 0)
		{
			lst = lst->next;
			len++;
		}
		return (len);
	}
	return (0);
}
